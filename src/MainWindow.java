    // CREATE NEW CLIENT IN DATABASE    
    private void btnSaveClientInfoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveClientInfoMouseClicked
        // OBTAIN FORM DATA
        String name = this.txtClientName.getText();
        String mail = this.txtClientMail.getText();
        String cellphone = this.txtClientTel.getText();
        String telcode = (String) this.cboxTelCodes.getSelectedItem();
        String full_number = telcode + cellphone;
        
        // CREATE NEW CLIENT
        Client new_client = new Client(name, full_number, mail);
        
        // ADD TO DATABASE
        if (!DB.insertClient(new_client)){
            JOptionPane.showMessageDialog(null, "Cliente ya existe en la base de datos.");
        }
                
        // RELOAD CLIENT LIST
        this.cboxClientList.setModel(clientComboboxModel());
        
        // CLEAR CLIENT DATA
        this.txtClientName.setFocusable(true);
        this.txtClientMail.setFocusable(true);
        this.txtClientTel.setFocusable(true);
    }//GEN-LAST:event_btnSaveClientInfoMouseClicked

    
    // FILL CLIENT INFO FROM DATABASE WHEN SELECTED
    private void cboxClientListItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboxClientListItemStateChanged
        // GET CLIENT MAIL
        String client_info = (String) this.cboxClientList.getSelectedItem();
        int separator_index = client_info.indexOf(" - ");
        String client_mail = client_info.substring(separator_index + 3);

        // FILL CLIENT INFO FROM DATABASE
        ResultSet client_data = (ResultSet) DB.searchClient(client_mail);
        try {
            // fech
            client_data.next();
            // fill data
            this.txtClientName.setText(client_data.getString("name"));
            this.txtClientTel.setText(client_data.getString("cellphone"));
            this.txtClientMail.setText(client_data.getString("email"));
            // fill client object
            case_client.setID(client_data.getInt("id"));
            case_client.setName(client_data.getString("name"));
            case_client.setCellphone(client_data.getString("cellphone"));
            case_client.setEmail(client_data.getString("email"));
            exercise_case.setClient(client_data.getInt("id"));
            // System.out.println(case_client.toString());
            // System.out.println(exercise_case.getClient());
        } 
        catch (SQLException ex) {
            Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cboxClientListItemStateChanged

    
