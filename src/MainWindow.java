    private float getCotizationPrice(float score){
        float price = (score * 50000) / 12;
        price = Math.max(price, 5000);        
        return price;
    }    

    private void showCotizationPrice(float price){
        this.lblCotizationPrice.setText("El valor estimado es: " + String.valueOf((int) Math.round(price)) + " CLP.");
        this.lblCotizationNote.setText("Nota: Debe pagarse la mitad ("+String.valueOf((int) Math.round(price/2))+"  CLP) por adelantado.");
    }
    
    
