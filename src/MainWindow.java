    // PERFORM COTIZATION
    private void btnExecuteCotizationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExecuteCotizationActionPerformed
        
        // GET CASE FORM DATA
        int exercise_type = exercise_case.getType();
        int programming_language = exercise_case.getLanguage();
        int exercise_amount = exercise_case.getExercise_number();
        int deadline = exercise_case.getDays();
        
        // CALCULATE SCORE
        float score;
        int extension_range;
        
        if (exercise_amount <= 5){extension_range=1;}
        else if (exercise_amount <= 10){extension_range=2;}
        else if (exercise_amount <= 15){extension_range=3;}
        else if (exercise_amount <= 20){extension_range=4;}
        else {extension_range=5;}
        
        if (deadline <= 3) {score = exercise_type + programming_language + extension_range;}
        else if (deadline <= 6 ){score = (float) ((exercise_type + programming_language + extension_range) * 0.9);}
        else if (deadline <= 9 ){score = (float) ((exercise_type + programming_language + extension_range) * 0.8);}
        else if (deadline <= 12 ){score = (float) ((exercise_type + programming_language + extension_range) * 0.6);}
        else {score = (float) ((exercise_type + programming_language + extension_range) * 0.4);}
        
        // CALCULATE PRICE
        float price = getCotizationPrice(score);
        exercise_case.setPrice(price);
        showCotizationPrice(price);
//        System.out.println("Servicio: " + exercise_type +
//                "\nProgramming language: " + programming_language + 
//                "\nExtension range: " + extension_range +
//                "\nDeadline: " + deadline +
//                "\nScore: " + score + 
//                "\nPrice: " + price);
    }//GEN-LAST:event_btnExecuteCotizationActionPerformed

